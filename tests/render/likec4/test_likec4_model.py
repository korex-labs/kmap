def test_likec4_model_header_helpers_preserve_rendered_line_format():
    assert likec4_system_header_line("sys_pay", "GoLang_App", 'Pay "API"') == '  sys_pay = GoLang_App "Pay \\"API\\"" {'
    assert likec4_container_header_line("ctr_pay_api", "container", "API") == '    ctr_pay_api = container "API" {'


def test_likec4_model_partition_helpers_split_external_project_and_orphan_systems():
    systems = [
        {"id": "sys.api", "project_id": "prj.api"},
        {"id": "sys.worker", "project_id": "prj.worker"},
        {"id": "sys.legacy", "project_id": "legacy"},
        {"id": "ext.docs", "tags": ["External"]},
        {"id": "ext.no.project"},
    ]

    assert [system["id"] for system in external_model_systems(systems)] == ["ext.docs", "ext.no.project"]
    assert [system["id"] for system in project_model_systems(systems, "prj.api")] == ["sys.api"]
    assert orphan_model_systems_by_project(systems, {"prj.api", "prj.worker"}) == {
        "legacy": [{"id": "sys.legacy", "project_id": "legacy"}]
    }


