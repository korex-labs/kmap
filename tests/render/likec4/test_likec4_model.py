def test_likec4_model_and_relation_files_are_split_by_project_and_external():
    architecture = {
        "projects": [
            {"id": "prj.demo.api", "name": "api", "title": "API"},
            {"id": "prj.demo.worker", "name": "worker", "title": "Worker"},
        ],
        "systems": [
            {"id": "sys.demo.api", "project_id": "prj.demo.api", "title": "API"},
            {"id": "sys.demo.worker", "project_id": "prj.demo.worker", "title": "Worker"},
            {"id": "ext.docs", "title": "docs.example.com", "element_type": "Website", "tags": ["External"]},
        ],
        "containers": [
            {"id": "ctr.demo.api.app", "system_id": "sys.demo.api", "title": "App"},
            {"id": "ctr.demo.worker.app", "system_id": "sys.demo.worker", "title": "Worker"},
        ],
        "relationships": [
            {"id": "rel.1", "source_id": "ctr.demo.api.app", "target_id": "ext.docs", "title": "docs"},
            {
                "id": "rel.2",
                "source_id": "ctr.demo.api.app",
                "target_id": "ctr.demo.worker.app",
                "title": "worker",
            },
        ],
    }

    model_files = render_likec4_model_files(architecture)
    relation_files = render_likec4_relation_files(architecture)

    assert "ext_docs" in model_files["model/00-external.c4"]
    assert "sys_demo_api" not in model_files["model/00-external.c4"]
    assert "sys_demo_api" in model_files["model/projects/api.c4"]
    assert "sys_demo_worker" not in model_files["model/projects/api.c4"]
    assert "ext_docs" in relation_files["model/relations/00-external.c4"]
    assert "sys_demo_worker.ctr_demo_worker_app" in relation_files["model/relations/api.c4"]


