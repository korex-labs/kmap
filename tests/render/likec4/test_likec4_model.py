def test_likec4_system_resource_lines_include_project_links():
    lines = likec4_system_resource_lines(
        {"project_id": "prj.pay"},
        {
            "prj.pay": {
                "name": "pay",
                "title": "Payments",
                "owner_team": "Platform",
                "domain_team": "Commerce",
                "description": "Payment service",
                "discovery": {"namespaces": ["payments", "payments"]},
                "resources": {"repo": "https://example.com/pay.git"},
            }
        },
    )

    assert "    metadata {" in lines
    assert '      project "pay"' in lines
    assert '      description "Payment service"' not in lines
    assert "repo" not in "\n".join(lines)
    assert "    link https://example.com/pay.git 'Repository'" in lines


def test_likec4_system_description_uses_system_then_project_description():
    projects = {"prj.pay": {"description": "Project description"}}

    assert likec4_system_description({"description": "System description", "project_id": "prj.pay"}, projects) == (
        "System description"
    )
    assert likec4_system_description({"project_id": "prj.pay"}, projects) == "Project description"


def test_likec4_system_resource_lines_skip_links_with_unsafe_url_syntax():
    lines = likec4_system_resource_lines(
        {"project_id": "prj.pay"},
        {
            "prj.pay": {
                "name": "pay",
                "resources": {"repo": 'https://example.com/"bad"\ninclude hacked'},
            }
        },
    )

    assert not any("repo" in line for line in lines)
    assert not any(line.strip().startswith("link ") for line in lines)


