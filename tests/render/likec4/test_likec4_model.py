def test_render_likec4_model_outputs_systems_containers_and_metadata():
    rendered = render_likec4_model(
        {
            "projects": [
                {
                    "id": "prj.pay",
                    "name": "pay",
                    "title": "Payments",
                    "description": "Payment service",
                    "discovery": {"namespaces": ["pay-prod"]},
                    "resources": {"repo": "https://example.com/pay.git"},
                    "tags": ["payments"],
                }
            ],
            "systems": [
                {
                    "id": "sys.pay",
                    "name": "pay",
                    "title": "Pay",
                    "project_id": "prj.pay",
                    "element_type": "GoLang_App",
                }
            ],
            "containers": [
                {
                    "id": "ctr.pay.api",
                    "name": "api",
                    "title": "API",
                    "system_id": "sys.pay",
                    "technology": "Go",
                    "discovery": {"clusters": ["cluster-1"]},
                    "runtime": {
                        "container_kinds": ["container"],
                        "images": ["example/api:1"],
                        "container_ports": ["8080/TCP"],
                    },
                    "name_source": {"raw_container_name": "api-prod"},
                }
            ],
        }
    )

    assert 'sys_pay = GoLang_App "Pay"' in rendered
    assert "    #payments" in rendered
    assert '  description "Payment service"' in rendered
    assert '      description "Payment service"' not in rendered
    assert "link https://example.com/pay.git 'Repository'" in rendered
    assert 'repo "https://example.com/pay.git"' not in rendered
    assert 'ctr_pay_api = container "API"' in rendered
    assert "      #payments" in rendered
    assert rendered.index("      #payments") < rendered.index('      technology "Go"')
    assert "      link https://example.com/pay.git 'Repository'" in rendered
    assert 'technology "Go"' in rendered
    assert 'namespaces "pay-prod"' not in rendered
    assert 'clusters "cluster-1"' not in rendered
    assert 'container_image "example/api:1"' in rendered
    assert 'ops_network "container ports: 8080/TCP"' in rendered
    assert 'container_source "api-prod"' in rendered
    assert 'container_images "example/api:1"' not in rendered
    assert 'container_ports "8080/TCP"' not in rendered


